CREATE TABLE IF NOT EXISTS OB_DCR_DETAILS (
  ID        VARCHAR(255)  NOT NULL,
  TPP_NAME VARCHAR(255) NOT NULL,
  REQUEST_HEADER NVARCHAR(MAX) NOT NULL,
  REQUEST   NVARCHAR(MAX) NOT NULL CHECK,
  CLIENT_ID VARCHAR(255) NOT NULL,
  SOFTWARE_ENV VARCHAR(255) NOT NULL,
  APPLICATION_ID INTEGER NOT NULL,
  SOFTWARE_ID VARCHAR(255) NOT NULL,
  SPEC_VERSION VARCHAR(64) NOT NULL,
  CREATED_TIMESTAMP DATETIME NOT NULL DEFAULT CURRENT_DATE(),
  STATUS VARCHAR(64) NOT NULL,
  CLID_ISSUED_AT INTEGER NOT NULL,
  STATUS_UPDATED_TIMESTAMP DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (ID),
);
